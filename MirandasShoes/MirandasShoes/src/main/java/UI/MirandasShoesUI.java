    private void btnPurchaseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPurchaseActionPerformed
        // TODO add your handling code here:
        if (tblShoes.getSelectedRow() > -1 && lstCustomers.getSelectedIndex() > -1) {
            int dialogButton = JOptionPane.YES_NO_OPTION;
            double subtotal = 0;
            int selectedRows[] = tblShoes.getSelectedRows();
            for (int i : selectedRows) {
                subtotal += Double.valueOf(tblShoes.getValueAt(i, 6).toString());
            }
            int dialogResult = JOptionPane.showConfirmDialog(null, "Subtotal: $" + subtotal + "\nTax: 15%" + "\nTotal: $" + String.format("%.2f",(subtotal * 1.15)) + "\nYour total is $" + String.format("%.2f",(subtotal * 1.15)) + ". Confirm purchase?", "Warning", dialogButton);
            if (dialogResult == JOptionPane.YES_OPTION) {
                ArrayList<Purchase> purchases = new ArrayList<>();
                for (int i : selectedRows) {
                    Purchase p = new Purchase();
                    p.setShoeID(tblShoes.getValueAt(i, 0).toString());
                    p.setCustomer(lstCustomers.getSelectedValue());
                    p.setTotal(Double.valueOf(tblShoes.getValueAt(i, 6).toString()));
                    purchases.add(p);
                }
                AddPurchase addPurchase = new AddPurchase();
                addPurchase.AddPurchase(purchases);
                retrieveShoes();
            }
        }
        else{
            JOptionPane.showMessageDialog(this, "Please select one or more shoes from the inventory and a customer");
        }
    }//GEN-LAST:event_btnPurchaseActionPerformed

