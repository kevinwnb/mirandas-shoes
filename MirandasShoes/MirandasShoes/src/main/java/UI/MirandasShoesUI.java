    private void retrieveShoes() {
        GetShoes ms = new GetShoes();
        DefaultTableModel tblShoesModel = (DefaultTableModel) tblShoes.getModel();
        int rows = tblShoesModel.getRowCount();
        for (int i = rows - 1; i >= 0; i--) {
            tblShoesModel.removeRow(i);
        }
        for (Shoe shoe : ms.shoeList) {
            tblShoesModel.addRow(new Object[]{shoe.getId(), shoe.getName(), shoe.getBrand(), shoe.getType(), shoe.getColor(), shoe.getSize(), shoe.getPrice()});
        }
        //lstShoes.setModel(model);
        tblShoes.setModel(tblShoesModel);
    }
    
    private void retrieveCustomers() {
        GetCustomers gc = new GetCustomers();
        DefaultListModel lm = new DefaultListModel();
        ArrayList<Customer> customers = gc.customerList;
        for (Customer c : customers) {
            lm.addElement(c.getFirstName() + " " + c.getLastName());
        }
        lstCustomers.setModel(lm);
    }

