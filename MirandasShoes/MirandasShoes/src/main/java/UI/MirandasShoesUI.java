    private void btnUpdateShoeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateShoeActionPerformed
        // TODO add your handling code here:
        if (tblShoes.getSelectedRow() > -1) {
            Shoe s = new Shoe();
            s.setId(tblShoes.getValueAt(tblShoes.getSelectedRow(), 0).toString());
            s.setName(tblShoes.getValueAt(tblShoes.getSelectedRow(), 1).toString());
            s.setBrand(tblShoes.getValueAt(tblShoes.getSelectedRow(), 2).toString());
            s.setType(tblShoes.getValueAt(tblShoes.getSelectedRow(), 3).toString());
            s.setColor(tblShoes.getValueAt(tblShoes.getSelectedRow(), 4).toString());
            s.setSize((double) tblShoes.getValueAt(tblShoes.getSelectedRow(), 5));
            s.setPrice((double) tblShoes.getValueAt(tblShoes.getSelectedRow(), 6));
            UpdateShoeUI updateShoe = new UpdateShoeUI(s);
            updateShoe.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
            updateShoe.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "Please select a shoe to update");
        }
    }//GEN-LAST:event_btnUpdateShoeActionPerformed

    private void btnDeleteShoeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteShoeActionPerformed
        // TODO add your handling code here:
        int dialogButton = JOptionPane.YES_NO_OPTION;
        int dialogResult = JOptionPane.showConfirmDialog(null, "Delete the selected shoes", "Warning", dialogButton);
        if (dialogResult == JOptionPane.YES_OPTION) {
            int selectedRows[] = tblShoes.getSelectedRows();
            ArrayList<Shoe> shoesToDelete = new ArrayList<Shoe>();
            for (int i : selectedRows) {
                Shoe s = new Shoe();
                s.setId(tblShoes.getValueAt(i, 0).toString());
                shoesToDelete.add(s);
            }
            DeleteShoes deleteShoe = new DeleteShoes();
            deleteShoe.DeleteShoe(shoesToDelete);
            retrieveShoes();
        }
    }//GEN-LAST:event_btnDeleteShoeActionPerformed

